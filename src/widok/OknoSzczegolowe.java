    private void tpBadaniaStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tpBadaniaStateChanged
        if (pDodajBadanie.isShowing()) {
            Object[] rodzajBadania = {"EKG", "USG"};
            String s = (String) JOptionPane.showInputDialog(
                    this,
                    "Nowe badanie...",
                    "Dodawanie nowego badania",
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    rodzajBadania,
                    "EKG");
            switch (s) {
                case "EKG": {
                    tpBadania.setSelectedComponent(pEkg);
                    lListaBadanEkg.setText("Dodawanie nowego badania EKG: ");
                    cbEkgLista.setVisible(false);
                    tfEkgId.setText("");
                    tfEkgPobyt.setText(String.valueOf(e.getPb().getId()));
                    tfEkgLekarz.setText("");
                    tfEkgDataBad.setText("");
                    tfEkgOpis.setText("");
                    tfEkgRytm.setText("");
                    tfEkgWar1.setText("");
                    tfEkgWar2.setText("");
                    break;
                }
                case "USG": {
                    tpBadania.setSelectedComponent(pUsg);
                    lListaBadanUsg.setText("Dodawanie nowego badania USG: ");
                    cbUsgLista.setVisible(false);
                    tfUsgId.setText("");
                    tfUsgPobyt.setText(String.valueOf(u.getPb().getId()));
                    tfUsgLekarz.setText("");
                    tfUsgDataBad.setText("");
                    tfUsgOpis.setText("");
                    cbUsgListaNazw.setSelectedItem(null);
                    break;
                }
            }
        }
    }//GEN-LAST:event_tpBadaniaStateChanged

    private void bZapiszUsgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bZapiszUsgActionPerformed
        uZrodlo = new UsgZrodlo();
        int weBId = tfUsgId.getText() == null || tfUsgId.getText().isEmpty() ? 0 : Integer.parseInt(tfUsgId.getText());
        int wePbId = Integer.parseInt(tfUsgPobyt.getText());
        int weBLk = Integer.parseInt(tfUsgLekarz.getText());
        Date weBDataBad = null;
        try {
            weBDataBad = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").parse(tfUsgDataBad.getText());
        } catch (ParseException ex) {
            Logger.getLogger(OknoSzczegolowe.class.getName()).log(Level.SEVERE, null, ex);
        }
        String weBOpis = tfUsgOpis.getText();
        int weUNId = cbUsgListaNazw.getSelectedItem() == null ? 0 : ((Usg) cbUsgListaNazw.getSelectedItem()).getNazwaId();

        try {
            if (weBId == 0) {
                weBId = uZrodlo.dodajBadanieUsg(wePbId, weBLk, weBDataBad, weBOpis, weUNId);
                tfUsgId.setText(String.valueOf(weBId));
                JOptionPane.showMessageDialog(this, "Dodano badanie " + weBId);
            } else {
                uZrodlo.aktualizujBadanieUsg(weBId, weBLk, weBDataBad, weBOpis, weUNId);
                JOptionPane.showMessageDialog(this, "Zaktualizowano badanie " + weBId);
            }
        } catch (SQLException ex) {
            Logger.getLogger(OknoSzczegolowe.class.getName()).log(Level.SEVERE, null, ex);
        }
        lListaBadanUsg.setText("Lista badań: ");
        cbUsgLista.setVisible(true);
    }//GEN-LAST:event_bZapiszUsgActionPerformed

