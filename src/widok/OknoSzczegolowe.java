    private void wczytajListeBadan(int rodzajBadania, JComboBox<Badanie> cbBadanieLista, JButton bUsunBadanie) throws SQLException {
        ArrayList<Badanie> listaBadan;
        listaBadan = bZrodlo.pobierzBadanieListaBD(pbOSZ.getId(), rodzajBadania);
        if (!listaBadan.isEmpty()) {
            for (Badanie b : listaBadan) {
                cbBadanieLista.addItem(b);
            }
        } else {
            bUsunBadanie.setEnabled(false);
        }
    }

    private void wczytajListeNazwUsg() throws SQLException {
        ArrayList<Usg> listaNazw;
        listaNazw = uZrodlo.pobierzNazwyWynikowUsg();
        for (Usg un : listaNazw) {
            cbUsgListaNazw.addItem(un);
        }
    }

